package com.windigo.sample.lastfm;

public class ImageResponse {

	private String text;
	private String size;
	public ImageResponse() {
	
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	
	
}
